# 1. A ValueError occurs when the user input the wrong value such as float value or a string instead of an integer.
# 2. A ZeroDivisionError occurs when the input for the denominator is zero and thus the fraction cannot be calculated.
# 3. The following is the changed code.

try:
    numerator = int(input("Enter the numerator: "))
    denominator = int(input("Enter the denominator: "))
    while denominator == 0:
        print("Denominator cannot be zero!")
        denominator = int(input("Enter the denominator: "))
    fraction = numerator / denominator
    print(fraction)
except ValueError:
    print("Numerator and denominator must be valid numbers!")
except ZeroDivisionError:
    print("Cannot divide by zero!")
print("Finished.")
